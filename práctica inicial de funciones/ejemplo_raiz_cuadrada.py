# función para calcular la raíz cuadrada
def calcular_raiz_cuadrada(n):
    ''' Comentarios multilínea en Python: pueden abarcar varias líneas.
    Inician y terminan con tres comillas: dobles o simples '''
    resul = n ** 0.5
    return resul

n = float(input("Calcular raíz cuadrada al número: "))
print(calcular_raiz_cuadrada(n))
