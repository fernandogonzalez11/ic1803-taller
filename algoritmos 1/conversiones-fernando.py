# programa 5: convertir un input en metros a otras unidades

m = int(input("digite la cantidad de metros: "))
print("kilómetros:", m * 0.001)
print("millas:", m * 0.00062137)
print("pies:", m * 3.2808)
print("yardas:", m * 1.0936)
print("centímetros:", m * 100)
print("pulgadas:", m * 39.37)